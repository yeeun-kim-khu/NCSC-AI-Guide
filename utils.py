def render_source_buttons(text: str):
    sources = re.findall(r"\[출처(?:-\d+)?\]\s*(https?://\S+)", text)
    if sources:
        st.markdown("") 
        cols = st.columns(min(3, len(sources)))
        unique_urls = list(dict.fromkeys([u.strip().rstrip(").,]") for u in sources]))
        for i, url in enumerate(unique_urls):
            with cols[i % len(cols)]:
                st.link_button("🌐 자세히 보기", url)